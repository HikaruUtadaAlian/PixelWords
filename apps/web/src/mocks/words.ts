/** Shuffle and pick a subset */
export function pickWords(count: number = 30): Word[] {
  const shuffled = [...MOCK_WORD_BANK].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, Math.min(count, shuffled.length))
}

/** Pick words by difficulty */
export function pickWordsByDifficulty(count: number = 10, difficulty: 'cet4' | 'cet6' | 'gre' = 'cet4'): Word[] {
  const filtered = MOCK_WORD_BANK.filter(w => {
    if (difficulty === 'cet4') return w.category === 'CET-4'
    if (difficulty === 'cet6') return w.category === 'CET-6'
    return w.category === 'GRE'
  })
  const shuffled = [...filtered].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, Math.min(count, shuffled.length))
}
